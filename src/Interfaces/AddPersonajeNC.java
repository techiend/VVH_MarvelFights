    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        JSONObject personajeNC = new JSONObject();
        
        if (!txtNombre.getText().isEmpty()){
            if((HombreG.isSelected()) || (MujerG.isSelected())){
                if((FallecidoS.isSelected()) || (FallecidoN.isSelected())){
                        personajeNC.put("nombrepesonajenc", txtNombre.getText());
                        personajeNC.put("nombrerealpesonajenc", txtNombreR.getText());
                        personajeNC.put("apellidopesonajenc", txtApellido.getText());
                        personajeNC.put("generopersonajenc", (!HombreG.isSelected()) ? MujerG.getText(): HombreG.getText());
                        personajeNC.put("fallecidopersonajenc", (!FallecidoS.isSelected()) ? FallecidoN.getText(): FallecidoS.getText());
                        personajeNC.put("relacion", txtRelacion.getSelectedItem().toString());
                        personajeNC.put("parentesco", txtParentesco.getText());
                       personajeNC.put("descripcion", txtDescripcion.getText());
                         //alias.put("descripcion", txtDescripcion.getText());
                 
                    
                        Categorias.AddPersonajeNC(personajeNC, personajeID);
                   
                        fillTable();
                        //fillcbParafernalia();
                }
                else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar si se encuentra vivo o muerto", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un genero", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Nombre no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

