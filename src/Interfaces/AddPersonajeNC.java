    public void emptyTable(){
        DefaultTableModel model = (DefaultTableModel) tablePersonajeNC.getModel();

        int filas = tablePersonajeNC.getRowCount();
        for (int i = 1; i <= filas; i++){
            model.removeRow(0);
        }
    }
    
    public void fillTable(){
        emptyTable();
        
        JSONArray listaPersonaje = Categorias.getPersonajeNCList(personajeID);
        DefaultTableModel model = (DefaultTableModel) tablePersonajeNC.getModel();        
        
        for (int i = 0; i<listaPersonaje.length(); i++){
            JSONObject personaje = listaPersonaje.getJSONObject(i);
            
//model.addRow(new Object[]{personaje.getInt("id"), personaje.getString("nameo"), personaje.getString("genero"), personaje.getString("estado")});            
model.addRow(new Object[]{personaje.getInt("id"), personaje.getString("nameo"), personaje.getString("namer"), personaje.getString("lname"), personaje.getString("genero"), personaje.getString("estado"), personaje.getString("relacion"), personaje.getString("parentesco"), personaje.getString("descripcion")});
        }
     }
     
     private void fillPanelModPersonajeNC(String id) {
        JSONObject PersonajeNC = Categorias.getPersonajeNCRelacion(id);
        
        txtIDPersonajeNC_m.setText(id);
        txtNombre_m.setText(PersonajeNC.getString("nameo"));
        txtNombreR_m.setText(PersonajeNC.getString("namer"));
        txtApellido_m.setText(PersonajeNC.getString("lname"));
        if (PersonajeNC.getString("genero").equals("Hombre")){
            HombreG_m.setSelected(true);
        }else{
            MujerG_m.setSelected(true);
        }
        //txtNombreR_m.setText(PersonajeNC.getString("estado"));
        if (PersonajeNC.getString("estado").equals("Si")){
            FallecidoS_m.setSelected(true);
        }else{
            FallecidoN_m.setSelected(true);
        }        
        txtRelacion_m1.setSelectedItem(PersonajeNC.getString("relacion"));
        txtParentesco_m.setText(PersonajeNC.getString("parentesco"));
        txtDescripcion_m.setText(PersonajeNC.getString("descripcion"));
        
        //txtDescripcion_m.setText(alias.getString("descripcion"));
        
    }

